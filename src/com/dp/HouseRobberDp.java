package com.dp;

/**
 * LeetCode Hot 100 - 198. 打家劫舍 (House Robber)
 *
 * ═══════════════════════════════════════════════════════════
 * 人为思考过程：
 * ═══════════════════════════════════════════════════════════
 * 1. 先理解约束条件：不能偷相邻的两间房屋，否则触发警报。这意味着一旦偷了某一家，下一家就
 *    必须跳过。
 * 2. 考虑最简单的情况：只有一间屋子时，只能偷它；有两间屋子时，因为不能同时偷相邻的，所以
 *    只能选金额较大的那一间。
 * 3. 从第三间开始，每遇到一间新屋子，都面临一个抉择：偷它还是不偷它？
 *    - 如果偷当前屋子 nums[i]，则不能偷前一间 nums[i-1]，总收益 = dp[i-2] + nums[i]
 *    - 如果不偷当前屋子，总收益保持为偷到前一家时的最大收益 dp[i-1]
 * 4. 这两种选择中取最大值，就是到达第 i 间屋子时的最优解，即 dp[i] = max(dp[i-2] + nums[i],
 *    dp[i-1])。
 * 5. 这符合动态规划的"最优子结构"性质：当前状态的最优解只依赖于前两个状态，可以顺序递推求解。
 * 6. 思考到这一步，整个问题就归约为依次计算 dp[0], dp[1], ..., dp[n-1]，最终答案就是
 *    dp[n-1]。
 *
 * ═══════════════════════════════════════════════════════════
 * 具体措施（算法步骤）：
 * ═══════════════════════════════════════════════════════════
 * 1. 读入房屋数量 n 和每间房屋的金额数组 nums。
 * 2. 边界处理：如果只有 1 间房屋，直接返回 nums[0]。
 * 3. 初始化 dp 数组：
 *    a. dp[0] = nums[0]（第一间只能偷它）
 *    b. dp[1] = max(nums[0], nums[1])（前两间中选较大的偷）
 * 4. 从 i = 2 到 n-1 循环填表：
 *    dp[i] = max(dp[i-2] + nums[i], dp[i-1])
 * 5. 输出 dp[n-1] 作为最终能偷到的最大金额。
 *
 * ═══════════════════════════════════════════════════════════
 * 复杂度分析：
 * ═══════════════════════════════════════════════════════════
 * 时间复杂度：O(n)
 *   - 需要遍历一次房屋数组，每个位置进行常数次比较和加法操作。
 * 空间复杂度：O(n)
 *   - 使用了一个长度为 n 的 dp 数组存储每个位置的最优值。可优化为 O(1)（滚动变量）。
 */

import java.util.Scanner;

public class HouseRobberDp {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        System.out.println("请输入有多少个屋子：");
        int n = scanner.nextInt();
        int[] nums = new int[n];
        System.out.println("输入每个屋子的金钱数目：");
        for (int i = 0; i < n; i++) {
            nums[i] = scanner.nextInt();
        }

        // 边界处理：只有一间房屋时直接返回
        if (nums.length == 1) {
            System.out.println(nums[0]);
        } else {
            // dp[i] 表示偷到第 i 间房屋时可获得的最大金额
            int[] dp = new int[nums.length];
            dp[0] = nums[0];                          // 第一间只能偷它
            dp[1] = Math.max(nums[0], nums[1]);       // 前两间选较大的偷

            // 从第 3 间开始递推：不偷||偷当前
            for (int i = 2; i < nums.length; i++) {
                dp[i] = Math.max(dp[i - 2] + nums[i], dp[i - 1]);
            }
            System.out.println("所偷得最大金钱数目为：" + dp[nums.length - 1]);
        }
    }
}

