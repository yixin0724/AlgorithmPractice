package com.dp;

/*
    动态规划的原理
动态规划与分治法类似，都是把大问题拆分成小问题，通过寻找大问题与小问题的递推关系，解决一个个小问题，最终达到解决原问题的效果。但不同的是，分治法在子问题和子子问题等上被重复计算了很多次，而动态规划则具有记忆性，通过填写表把所有已经解决的子问题答案纪录下来，在新问题里需要用到的子问题可以直接提取，避免了重复计算，从而节约了时间，所以在问题满足最优性原理之后，用动态规划解决问题的核心就在于填表，表填写完毕，最优解也就找到。

最优性原理是动态规划的基础，最优性原理是指"多阶段决策过程的最优决策序列具有这样的性质：不论初始状态和初始决策如何，对于前面决策所造成的某一状态而言，其后各阶段的决策序列必须构成最优策略"。

 */

/* 用动态规划问题去解决数塔问题
   输入：二维数组a[n][n]
   输出：数塔的最大数值和其所经过的路径
   1. 初始化数值maxAdd的最后一行为数塔的底层数据：
      for(j=0;j<n;j++)
         maxAdd[n-1][j]=a[n-1][j];
   2. 从第n-1层开始直到第一层对下三角元素maxAdd[i][j]执行下述操作：
      2.1 maxAdd[i][j]=a[i][j]+max{maxAdd[i+1][j],maxAdd[i+1][j+1]};
      2.2 如果选择下标j的元素，则path[i][j]=j,否则path[i][j]=j+1;
   3. 输出最大数值和maxAdd[0][0];
   4.根据path数组确定每一层决策的列下标，输出路径信息。
 */
/*
这里面path数组二维的二维数组记录
path数组定义为二维数组，path[i] [j]，其值表示i层第j个元素的i+1层走向，
也就是说path数组记录下了所有可能的路径，到时候根据顶层的path记录的值向下递推就可以求出路径（顶层的值是代表下一层的走向）。
一维的
根据利用数组索引和值的配合，也可以通过一维数组实现记录，索引代表当前的层，值代表当前层的第几个元素，每层决策完后的最大值就是确定的路径，把它记录下来即可
 */

import java.util.Scanner;

/**
 * LeetCode Hot 100 - 120. 三角形最小路径和（最大路径变体）(Triangle - Max Path Variant)
 *
 * ═══════════════════════════════════════════════════════════
 * 人为思考过程：
 * ═══════════════════════════════════════════════════════════
 * 1. 先观察数塔（数字三角形）的结构：从上到下，每一层比上一层多一个数字，从顶层出发，每到
 *    一个数字 (i,j)，只能向下走到 (i+1,j) 或 (i+1,j+1)，最终走到底层。
 * 2. 暴力的办法是枚举所有路径，但路径数量是 2^(n-1) 级别的，当 n 稍大时完全不可行。
 * 3. 考虑从底层向上倒推：如果已经知道从第 i+1 层的每个位置出发到达底层的最优值，那么在第 i
 *    层的某个位置 (i,j)，只需要比较它下面两个位置 (i+1,j) 和 (i+1,j+1) 的最优值，选较大的
 *    加上当前位置的数字即可。
 * 4. 这就是自底向上的动态规划思路：从最后一行开始初始化，逐层往上填表，避免重复计算，每层
 *    每个位置只计算一次。
 * 5. 为了输出具体路径，用 path[i][j] 记录在 (i,j) 位置时选择了下一层的哪一列（j 或 j+1），
 *    填表完成后从 (0,0) 开始沿着 path 向下追踪。
 * 6. 最终 maxAdd[0][0] 就是整个数塔的最大路径和。
 *
 * ═══════════════════════════════════════════════════════════
 * 具体措施（算法步骤）：
 * ═══════════════════════════════════════════════════════════
 * 1. 读入数塔层数 n，按上三角形格式读入二维数组 a[n][n]。
 * 2. 初始化 maxAdd 表的最后一行为 a 的最后一行的值。
 * 3. 从第 n-2 层向上到第 0 层，对每个位置 (i,j)：
 *    a. 比较 maxAdd[i+1][j] 和 maxAdd[i+1][j+1]，取较大值。
 *    b. maxAdd[i][j] = a[i][j] + 上一步的较大值。
 *    c. 记录 path[i][j]：选 j 还是 j+1。
 * 4. 输出 maxAdd[0][0] 作为最大路径和。
 * 5. 从 path[0][0] 开始向下追踪，逐层输出路径上的数字。
 *
 * ═══════════════════════════════════════════════════════════
 * 复杂度分析：
 * ═══════════════════════════════════════════════════════════
 * 时间复杂度：O(n^2)
 *   - 需要遍历下三角矩阵的所有位置，即 1+2+...+n = n(n+1)/2 = O(n^2)。
 * 空间复杂度：O(n^2)
 *   - maxAdd 表和 path 表各占用 n*n 空间。
 */

public class NumberTriangleMaxPathDp {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("请输入数塔最后一层的个数：");
        int n = sc.nextInt();
        int a[][] = new int[n][n];
        System.out.println("请从上往下依次赋值：");
        for (int i = 0; i < n; i++) {
            for (int j = 0; j <= i; j++) {
                a[i][j] = sc.nextInt();
            }
        }
        System.out.println("所得最大路径之和为：" + countTower(a, n));
    }

    public static int countTower(int a[][], int n) {
        // maxAdd[i][j] 表示从 (i,j) 出发到达底层能获得的最大路径和
        int maxAdd[][] = new int[n][n];
        // path[i][j] 记录在 (i,j) 位置时选择了下一层的哪一列（用于输出路径）
        int path[][] = new int[n][n];

        // 初始化：最后一层的最优值就是自身的值（已经到底了）
        for (int i = 0; i < n; i++) {
            maxAdd[n - 1][i] = a[n - 1][i];
        }

        // 自底向上逐层计算
        for (int i = n - 2; i >= 0; i--) {
            for (int j = 0; j <= i; j++) {
                // 比较下一层的两个可达位置，选择较大的路径
                if (maxAdd[i + 1][j] > maxAdd[i + 1][j + 1]) {
                    maxAdd[i][j] = a[i][j] + maxAdd[i + 1][j];
                    path[i][j] = j;                 // 选择左下方
                } else {
                    maxAdd[i][j] = a[i][j] + maxAdd[i + 1][j + 1];
                    path[i][j] = j + 1;             // 选择右下方
                }
            }
        }

        // 输出路径：从顶层开始，沿着 path 逐层追踪
        System.out.printf("路径为：" + a[0][0]);
        int j = path[0][0];                         // 顶层决策指向的下一层列号
        for (int i = 1; i < n; i++) {
            System.out.printf("-->" + a[i][j]);
            j = path[i][j];                         // 本层决策指向的下一层列号
        }
        System.out.println("");
        return maxAdd[0][0];                        // 顶层的最优值即为全局最大路径和
    }
}

