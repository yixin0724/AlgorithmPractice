package com.dp;

import java.util.Scanner;

/**
 * LeetCode Hot 100 - 1143. 最长公共子序列 (Longest Common Subsequence)
 *
 * ═══════════════════════════════════════════════════════════
 * 人为思考过程：
 * ═══════════════════════════════════════════════════════════
 * 1. 先明确"子序列"的定义：子序列不要求连续，只需保持原始顺序即可。例如 "abc" 是 "aebdc"
 *    的子序列。
 * 2. 最直接的想法是枚举 X 的所有子序列，看哪些也是 Y 的子序列，取最长的。但子序列数量是
 *    2^m 级别，不可行。
 * 3. 考虑用动态规划缩小问题规模：设 L[i][j] 表示"X 的前 i 个字符与 Y 的前 j 个字符的最长
 *    公共子序列长度"。这样就能逐步构建出最优解。
 * 4. 分析末尾字符 X[i-1] 和 Y[j-1] 的关系：
 *    - 如果相等（X[i-1] == Y[j-1]），则这个字符一定属于最长公共子序列，问题退化为
 *      L[i-1][j-1] + 1。因为前面字符的最优解加上当前匹配的字符就是当前位置的最优解。
 *    - 如果不相等，那么当前字符不能同时被选取，只能在"跳过 X 的末尾"或"跳过 Y 的末尾"中
 *      选较优的，即 L[i][j] = max(L[i-1][j], L[i][j-1])。
 * 5. 这个递推式覆盖了所有可能情况，从 i=1,j=1 开始向上填表即可。
 * 6. 填表完成后 L[m][n] 就是最长公共子序列的长度。为了还原具体子序列内容，需要一个状态矩阵
 *    S 记录每次决策的方向（1=来自左上、2=来自左侧、3=来自上方），填表后从右下角回溯即可
 *    构造出子序列。
 *
 * ═══════════════════════════════════════════════════════════
 * 具体措施（算法步骤）：
 * ═══════════════════════════════════════════════════════════
 * 1. 读入两个字符串 X 和 Y，转为字符数组。
 * 2. 初始化二维表 L[n+1][m+1]（长度表）和 S[n+1][m+1]（状态表），第 0 行和第 0 列都为 0。
 * 3. 双重循环填表（i 从 1 到 n，j 从 1 到 m）：
 *    a. 若 X[i-1] == Y[j-1]：L[i][j] = L[i-1][j-1] + 1，S[i][j] = 1。
 *    b. 否则若 L[i][j-1] >= L[i-1][j]：L[i][j] = L[i][j-1]，S[i][j] = 2。
 *    c. 否则：L[i][j] = L[i-1][j]，S[i][j] = 3。
 * 4. 创建字符数组 z 储存结果，从右下角按 S 表回溯：
 *    - S[i][j] == 1：当前字符匹配，加入结果，i--, j--。
 *    - S[i][j] == 2：来自左侧，j--。
 *    - S[i][j] == 3：来自上方，i--。
 * 5. 输出最长公共子序列内容和长度 L[n][m]。
 *
 * ═══════════════════════════════════════════════════════════
 * 复杂度分析：
 * ═══════════════════════════════════════════════════════════
 * 时间复杂度：O(n*m)
 *   - 需要双层循环遍历所有 (i,j) 组合，每个状态进行常数次操作。
 * 空间复杂度：O(n*m)
 *   - L 表和 S 表各占用 (n+1)*(m+1) 空间，需要同时保存以便回溯。
 */

public class LongestCommonSubsequenceDp {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("请直接输入序列X：");
        char[] str1 = sc.nextLine().toCharArray();
        System.out.println("请直接输入序列Y：");
        char[] str2 = sc.nextLine().toCharArray();

        System.out.println("最长的公共子序列长度为：" + CommonOrder(str1, str1.length, str2, str2.length));
    }

    public static int CommonOrder(char[] X, int n, char[] Y, int m) {
        // L[i][j] 表示 X 的前 i 个字符与 Y 的前 j 个字符的最长公共子序列长度
        int[][] L = new int[n + 1][m + 1];
        // S[i][j] 记录状态转移方向：1=左上(匹配), 2=来自左, 3=来自上
        int[][] S = new int[n + 1][m + 1];

        // 初始化第 0 行和第 0 列为 0（空序列的 LCS 长度为 0）
        for (int i = 0; i < m + 1; i++) {
            L[0][i] = 0;
        }
        for (int i = 0; i < n + 1; i++) {
            L[i][0] = 0;
        }

        // 双重循环填表
        for (int i = 1; i < n + 1; i++) {
            for (int j = 1; j < m + 1; j++) {
                if (X[i - 1] == Y[j - 1]) {
                    // 末尾字符相等：可以从左上角状态 +1 转移
                    L[i][j] = L[i - 1][j - 1] + 1;
                    S[i][j] = 1;        // 标记为匹配
                } else if (L[i][j - 1] >= L[i - 1][j]) {
                    // 不等且左侧更优：跳过 Y 的当前字符
                    L[i][j] = L[i][j - 1];
                    S[i][j] = 2;        // 标记来自左侧（Y 移动）
                } else {
                    // 不等且上方更优：跳过 X 的当前字符
                    L[i][j] = L[i - 1][j];
                    S[i][j] = 3;        // 标记来自上方（X 移动）
                }
            }
        }

        // 回溯构造最长公共子序列
        int i = n;
        int j = m;
        int k = L[n][m];                // k 为最长公共子序列的长度
        char[] z = new char[k + 1];     // z 储存结果子序列

        // 根据 S 状态矩阵反向追踪
        while (i > 0 && j > 0) {
            if (S[i][j] == 1) {         // 匹配状态：当前字符属于 LCS
                z[k - 1] = X[i - 1];
                k--;
                i--;
                j--;
            } else if (S[i][j] == 2) {  // 来自左侧：跳过 Y 字符
                j--;
            } else {                    // 来自上方：跳过 X 字符
                i--;
            }
        }

        // 输出最长公共子序列内容
        System.out.print("得到的公共子序列为：");
        for (k = 0; k < L[n][m]; k++) {
            if (k < L[n][m] - 1) {
                System.out.print(z[k] + ",");
            } else {
                System.out.println(z[k]);
            }
        }
        return L[n][m];
    }
}

