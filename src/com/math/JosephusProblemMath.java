package com.math;

import java.util.Scanner;

/**
 * 约瑟夫环问题（数学递推解法）
 *
 * ═══════════════════════════════════════════════════════════
 * 人为思考过程：
 * ═══════════════════════════════════════════════════════════
 * 面对"n 个人围成一圈、每次数到 k 出局、求最后幸存者"这个问题，最直观的方法是模拟——用数组或链表一圈一圈地数。但能不能从数学上直接推出来？
 *
 * 1. 先考虑最简单的情况：当只有 1 个人时，无论怎么数，最后剩下的一定是 0 号（0-based）。
 * 2. 尝试从 i-1 个人的解推导出 i 个人的解：
 *    - 当有 i 个人时，第一轮会淘汰编号为 (k-1) % i 的人。
 *    - 淘汰后，从淘汰者下一个位置重新编号，相当于剩下的 i-1 个人重排编号，形成了规模为 i-1 的子问题。
 * 3. 设 f(i) 表示 i 个人时幸存者的 0-based 编号，那么：
 *    - 第一轮淘汰 (k-1) % i 之后，原来的编号 x 在新的编号体系中变成了 (x - k) % i。
 *    - 反过来，子问题的答案 y 映射回原编号体系就是 (y + k) % i。
 * 4. 从而得出递推公式：f(i) = (f(i-1) + k) % i，初始条件 f(1) = 0。
 * 5. 最后将 0-based 答案转为 1-based 编号即可。
 * 6. 这个推导过程非常精妙——不需要任何模拟，只需要一个循环就能算出答案。
 *
 * ═══════════════════════════════════════════════════════════
 * 具体措施（算法步骤）：
 * ═══════════════════════════════════════════════════════════
 * 1. 初始化幸存者编号 survivor = 0（1 个人时的 0-based 解）。
 * 2. 从 i = 2 到 n 依次递推：
 *    - survivor = (survivor + k) % i
 * 3. 返回 survivor + 1，将 0-based 转为 1-based 编号。
 *
 * ═══════════════════════════════════════════════════════════
 * 复杂度分析：
 * ═══════════════════════════════════════════════════════════
 * 时间复杂度：O(n)
 *   - n 为总人数，只需一次从 2 到 n 的循环。
 * 空间复杂度：O(1)
 *   - 只保存当前递推结果 survivor，无额外空间消耗。
 */
public class JosephusProblemMath {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        System.out.println("请输入人数 n 和报数间隔 k：");
        int n = scanner.nextInt();
        int k = scanner.nextInt();

        // survivor 表示 i 个人时的幸存者 0-based 编号，初始为 1 个人的情况
        int survivor = 0;

        // 从 2 人到 n 人依次递推
        for (int i = 2; i <= n; i++) {
            survivor = (survivor + k) % i;
        }

        // 转为 1-based 编号输出
        System.out.println("最后剩下的人编号为：" + (survivor + 1));
    }
}
