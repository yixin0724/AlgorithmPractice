package com.hashing;

/**
 * LeetCode Hot 100 - 387. First Unique Character in a String（字符串中的第一个唯一字符）
 *
 * ═══════════════════════════════════════════════════════════
 * 人为思考过程：
 * ═══════════════════════════════════════════════════════════
 * 1. 题目要求在字符串中找出只出现一次的小写字母。
 *    例如 "abacbc" 中没有只出现一次的字母，而 "abacbcd" 中 'd' 只出现一次。
 * 2. 看到"统计次数"这种需求，第一反应就是用哈希表。
 *    不过由于只涉及 26 个小写字母，用一个长度为 26 的整数数组
 *    来模拟哈希表会更简洁高效。
 * 3. 如何把字母映射到数组下标？
 *    'a' 的 ASCII 码是 97，'b' 是 98...
 *    所以用 c - 97（或 c - 'a'）就能把 'a' 映射到 0，'z' 映射到 25。
 * 4. 第一次遍历字符串：每遇到一个字符，就在对应数组位置加 1，
 *    遍历完数组 map[i] 就表示字母 (char)(i+97) 的出现次数。
 * 5. 第二次遍历数组 map：检查每个位置的值是否为 1，
 *    如果是 1 就输出该下标对应的字母。
 * 6. 这个方法利用了小写字母只有 26 个的有限字符集特性，
 *    用数组替代通用 HashMap，代码更简单，常数也更小。
 *
 * ═══════════════════════════════════════════════════════════
 * 具体措施（算法步骤）：
 * ═══════════════════════════════════════════════════════════
 * 1. 创建长度为 26 的整型数组 map，所有元素初始为 0。
 * 2. 第一次遍历：遍历字符串中每个字符 c，执行 map[c - 97]++。
 *    （将字母映射到 0~25 的下标并累加出现次数。）
 * 3. 第二次遍历：遍历数组 map[0..25]，
 *    若 map[i] == 1，输出字符 (char)(i + 97)。
 *
 * ═══════════════════════════════════════════════════════════
 * 复杂度分析：
 * ═══════════════════════════════════════════════════════════
 * 时间复杂度：O(n)
 *   - 遍历字符串一次 O(n)，再遍历固定大小 26 的数组一次 O(1)，
 *     总复杂度 O(n)。
 * 空间复杂度：O(1)
 *   - 计数数组固定为 26 个元素，不随输入字符串长度增长。
 */
public class UniqueCharacterHashing {
    public static void main(String[] args) {
        java.util.Scanner scanner = new java.util.Scanner(System.in);
        System.out.println("输入你要查找的字符串：");
        String s = scanner.nextLine();
        // 长度为 26 的数组模拟哈希表，map[0] 对应 'a'，map[25] 对应 'z'
        int[] map = new int[26];
        // 第一次遍历：统计每个字母的出现次数
        for (char c : s.toCharArray()) {
            map[c - 97]++;
        }
        // 第二次遍历：找到值为 1 的下标，映射为字母输出
        System.out.println("出现次数为1的字母有：");
        for (int i = 0; i < 26; i++) {
            if (map[i] == 1) {
                System.out.println((char) (i + 97));
            }
        }
    }
}
