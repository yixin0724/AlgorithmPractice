package com.bruteforce;

import java.util.Scanner;

/**
 * LeetCode Hot 100 - 215. Kth Largest Element in an Array（数组中的第K个最大元素）
 *
 * 说明：本题暴力解法可同时用于查找第 k 小元素或第 k 大元素，
 *       只需调整比较方向即可。此处演示的是查找第 k 小元素。
 *
 * ═══════════════════════════════════════════════════════════
 * 人为思考过程：
 * ═══════════════════════════════════════════════════════════
 * 1. 问题要求找出数组中第 k 小的元素。例如第 1 小就是最小值，
 *    第 2 小就是第二小的值，以此类推。
 * 2. 最简单的想法：我能不能重复 k 次"找最小值"这个动作？
 *    每次找到当前最小值之后，把它"标记为已使用"，
 *    然后下一轮再从剩余元素中找最小值。
 * 3. 如何"标记为已使用"？因为数组元素是正整数，
 *    可以把已找到的最小值替换成一个很大的哨兵值（如 9999），
 *    这样下一轮扫描时它就不会再被选为最小值。
 * 4. 用 while 循环控制轮数，每轮用 for 遍历整个数组找当前最小值，
 *    记录其值和位置，然后将其替换为哨兵值，计数器加 1。
 * 5. 当计数器到达 k 时，最后一轮找到的 min 就是第 k 小的元素。
 * 6. 这种暴力"逐轮摘除最小值"的思路非常直观，不需要任何特殊数据结构，
 *    但效率不高。
 *
 * ═══════════════════════════════════════════════════════════
 * 具体措施（算法步骤）：
 * ═══════════════════════════════════════════════════════════
 * 1. 读取数组长度和数组元素。
 * 2. 读取目标 k。
 * 3. 初始化计数器 flag = 0，while(flag != k) 循环 k 轮。
 * 4. 每轮中，遍历整个数组找到当前最小值 min 及其下标 index。
 * 5. 将 arr[index] 设置为哨兵值 9999，避免下一轮再次选中。
 * 6. flag++，进入下一轮。
 * 7. 循环结束后，最后一次记录的 min 即为第 k 小的元素。
 *
 * ═══════════════════════════════════════════════════════════
 * 复杂度分析：
 * ═══════════════════════════════════════════════════════════
 * 时间复杂度：O(kn)
 *   - 每轮需要遍历整个数组 O(n)，共执行 k 轮，
 *     最坏情况下 k = n，复杂度为 O(n^2)。
 * 空间复杂度：O(1)
 *   - 只使用 flag、min、index 等常数个临时变量。
 */
public class KthSmallestBruteForce {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("请输入数组的长度：");
        int length = sc.nextInt();
        int[] arr = new int[length];
        System.out.println("请依次为数组赋值：");
        for (int i = 0; i < length; i++) {
            arr[i] = sc.nextInt();
        }
        System.out.println("请输入你要选择第几个最小的数：");
        int k = sc.nextInt();
        System.out.println("你要查找的数为：" + selectMin(arr, k));
    }

    public static int selectMin(int[] arr, int k) {
        int flag = 0;
        int min = 0;
        int index = 0;
        // 循环 k 次，每次找出当前数组中的最小值
        while (flag != k) {
            min = 999;
            // 遍历数组找最小值及其位置
            for (int j = 0; j < arr.length; j++) {
                if (arr[j] < min) {
                    min = arr[j];
                    index = j;      // 记录最小值所在下标
                }
            }
            // 将已找到的最小值替换为哨兵值，防止下一轮再次选中
            arr[index] = 9999;
            flag++;                 // 已找到第 flag 小的元素
        }
        // 返回第 k 小的元素
        return min;
    }
}
