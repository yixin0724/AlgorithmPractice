package com.numbertheory;

import java.util.Scanner;

/**
 * 最大公约数——欧几里得算法
 *
 * ═══════════════════════════════════════════════════════════
 * 人为思考过程：
 * ═══════════════════════════════════════════════════════════
 * 求两个数的最大公约数，最朴素的想法是枚举所有可能的公约数，但面对两个亿级别的数字时这完全不现实。
 *
 * 1. 思考：如果 a 和 b 有最大公约数 g，那么 g 也一定能整除 a - b的任意整数倍。
 * 2. 具体来说，gcd(a, b) = gcd(b, a - k*b)，其中 k 可以是任意整数。
 * 3. 最自然的取法是令 k 为 a 除以 b 的商，即 k = a / b，这样 a - k*b 就是 a % b（余数）。
 * 4. 所以 gcd(a, b) = gcd(b, a % b)，问题规模从 (a, b) 缩小到了 (b, a % b)。
 * 5. 由于余数一定小于除数，每次递归（或迭代）两个数都会显著减小，最终 b 会变成 0。
 * 6. 当 b = 0 时，a 就是最大公约数。
 * 7. 这就是欧几里得算法，又名辗转相除法——两千多年前的智慧至今仍然是效率最优的方案。
 * 8. 直观理解就是：用较大的数反复减去较小的数的倍数，直到无法再减为止，剩下的就是公约数。
 *
 * ═══════════════════════════════════════════════════════════
 * 具体措施（算法步骤）：
 * ═══════════════════════════════════════════════════════════
 * 1. 对输入的 a 和 b 取绝对值（处理负数情况）。
 * 2. 循环执行以下步骤，直到 b == 0：
 *    a) 计算余数 remainder = a % b。
 *    b) 令 a = b，b = remainder。
 * 3. 返回 a，此时 a 即为最大公约数。
 *
 * ═══════════════════════════════════════════════════════════
 * 复杂度分析：
 * ═══════════════════════════════════════════════════════════
 * 时间复杂度：O(log min(a, b))
 *   - 每次迭代至少将较大的数减半，迭代次数为对数级别。
 * 空间复杂度：O(1)
 *   - 使用迭代实现，仅需常数变量。
 */
public class GreatestCommonDivisorNumberTheory {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        System.out.println("请输入两个整数：");
        int a = scanner.nextInt();
        int b = scanner.nextInt();
        System.out.println("最大公约数为：" + gcd(a, b));
    }

    /**
     * 欧几里得算法（辗转相除法）求最大公约数
     *
     * @param a 第一个整数
     * @param b 第二个整数
     * @return a 和 b 的最大公约数
     */
    public static int gcd(int a, int b) {
        // 处理负数情况
        a = Math.abs(a);
        b = Math.abs(b);

        // 辗转相除：gcd(a, b) = gcd(b, a % b)
        while (b != 0) {
            int remainder = a % b;
            a = b;
            b = remainder;
        }
        return a;
    }
}
