package com.recursion;

import java.util.Scanner;

/**
 * 阶乘（递归解法）
 *
 * ═══════════════════════════════════════════════════════════
 * 人为思考过程：
 * ═══════════════════════════════════════════════════════════
 * 阶乘问题几乎是最经典、最纯粹的递归入门例子，因为它的定义本身就具有递归结构。
 *
 * 1. 阶乘的定义：n! = n * (n-1) * (n-2) * ... * 2 * 1。
 * 2. 仔细观察这个定义，可以发现：n! = n * (n-1)!。这就是递归的核心——把一个大问题分解成一个规模更小的同类子问题。
 * 3. 类似地，(n-1)! = (n-1) * (n-2)!，直到 1! = 1 或 0! = 1，此时不能再拆分了——这就是递归的终止条件（base case）。
 * 4. 递归函数 factorial(n) 的逻辑就是：
 *    - 如果 n <= 1，直接返回 1（终止条件）。
 *    - 否则返回 n * factorial(n-1)（递归调用，问题规模缩小）。
 * 5. 递归的执行过程就像一个"栈"——每次调用都会压入一层，直到触底（base case），然后逐层返回计算结果。
 * 6. 虽然递归写法简洁优雅，但要注意栈溢出问题：如果 n 太大，递归深度过深可能导致 StackOverflowError。
 *
 * ═══════════════════════════════════════════════════════════
 * 具体措施（算法步骤）：
 * ═══════════════════════════════════════════════════════════
 * 1. 定义递归函数 factorial(n)：
 *    a) 终止条件：n <= 1，返回 1。
 *    b) 递归关系：返回 n * factorial(n - 1)。
 * 2. 主函数读取输入 n，调用 factorial(n) 并输出结果。
 *
 * ═══════════════════════════════════════════════════════════
 * 复杂度分析：
 * ═══════════════════════════════════════════════════════════
 * 时间复杂度：O(n)
 *   - n 为输入数字，递归调用 n 次，每次 O(1)。
 * 空间复杂度：O(n)
 *   - 递归调用栈深度为 n，每层保存局部变量和返回地址。
 */
public class FactorialRecursion {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        System.out.println("请输入 n：");
        int n = scanner.nextInt();
        System.out.println(n + " 的阶乘为：" + factorial(n));
    }

    /**
     * 递归计算 n 的阶乘
     *
     * @param n 非负整数
     * @return n 的阶乘
     */
    public static long factorial(int n) {
        // 终止条件：0! = 1! = 1
        if (n <= 1) {
            return 1;
        }
        // 递归关系：n! = n * (n-1)!
        return n * factorial(n - 1);
    }
}
