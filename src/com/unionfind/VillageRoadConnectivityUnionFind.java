package com.unionfind;

import java.util.Scanner;

/**
 * 村村通道路连通问题（并查集）
 *
 * ═══════════════════════════════════════════════════════════
 * 人为思考过程：
 * ═══════════════════════════════════════════════════════════
 * 面对"判断村庄之间的连通性、统计还需要建多少条路"这一类问题，首先需要理解问题的本质——这是一个图的连通分量问题。
 *
 * 1. 直观思考：每个村庄是一个节点，每条已有的路是一条边，构成了一个无向图。
 * 2. 目标：要保证所有村庄互通，意味着图必须是一个连通图（只有一个连通分量）。
 * 3. 如果图中有 k 个连通分量，那么至少需要 k-1 条边才能把 k 个分量全部连起来。
 * 4. 那么问题就转化为：如何高效地维护和查询哪些节点在同一个连通分量中？
 * 5. 并查集（Union-Find）就是为这种场景量身定做：它支持"合并两个集合"和"查询元素属于哪个集合"两种操作。
 * 6. 实现方式：用一个父节点数组 fa，初始时每个节点都是自己的父亲（自成一派）。
 *    - find(x)：沿着父节点链向上追溯，直到找到根（自己的父亲是自己）。同时做"路径压缩"——让沿途节点直接指向根，加速后续查询。
 *    - unity(x, y)：找到 x 和 y 各自的根，然后将一个根的父指针指向另一个根（合并两棵树）。
 * 7. 全部合并后，统计有多少个节点的 find(i) == i（即独立根的数量），这就是连通分量数 k。
 * 8. 答案 = k - 1。
 *
 * ═══════════════════════════════════════════════════════════
 * 具体措施（算法步骤）：
 * ═══════════════════════════════════════════════════════════
 * 1. 初始化并查集：fa[i] = i（每个元素自成一个集合）。
 * 2. 对于每条已有道路 (x, y)，调用 unity(x, y, fa) 合并。
 * 3. 遍历所有村庄，统计 find(i, fa) == i 的数量，即为连通分量数 ans。
 * 4. 输出 ans - 1（还需要建设的道路数量）。
 *
 * ═══════════════════════════════════════════════════════════
 * 复杂度分析：
 * ═══════════════════════════════════════════════════════════
 * 时间复杂度：O((n + m) * alpha(n))
 *   - n 为村庄数，m 为已有道路数。
 *   - find 操作在路径压缩优化下均摊接近 O(1)，alpha(n) 为反阿克曼函数，增长极慢。
 * 空间复杂度：O(n)
 *   - 需要长度为 n+1 的 fa 数组。
 */
public class VillageRoadConnectivityUnionFind {
    public static void main(String[] args) {
        // 父节点数组，fa[i] 表示 i 的父节点，根节点的父节点是自己
        int fa[] = new int[1000001];
        Scanner sc = new Scanner(System.in);

        while (true) {
            System.out.println("请输入有几个村庄和几条路：");
            int ans = 0;                    // ans 需要在每次循环中重置为 0
            int n = sc.nextInt();
            if (n == 0) {
                System.exit(0);
            }
            int m = sc.nextInt();

            // 初始化并查集：每个村庄最初都是自己的祖先
            for (int i = 1; i <= n; i++) {
                fa[i] = i;
            }

            System.out.println("请输入路所连接的村庄");
            for (int i = 1; i <= m; i++) {
                int x = sc.nextInt();
                int y = sc.nextInt();
                // 将已有道路两端的村庄合并到同一集合
                unity(x, y, fa);
            }

            // 统计连通分量数量（根节点的数量）
            for (int i = 1; i <= n; i++) {
                if (find(i, fa) == i) {     // 自己是自己的祖先，即为根节点
                    ans++;
                }
            }

            // k 个连通分量至少需要 k-1 条边才能全部连通
            System.out.println("还需要建设" + String.valueOf(ans - 1) + "条路");
        }
    }

    /**
     * 并查集查找——找到 x 所属集合的根节点（带路径压缩优化）
     */
    public static int find(int x, int fa[]) {
        // 当 x 不是自己的父节点时（即不是根），继续向上追溯
        if (x != fa[x]) {
            // 路径压缩：让沿途节点直接指向最终的根
            fa[x] = find(fa[x], fa);
        }
        return fa[x];  // 返回根节点
    }

    /**
     * 并查集合并——将 x 和 y 所在的集合合并
     */
    public static void unity(int x, int y, int[] fa) {
        int r1 = find(x, fa);   // 找到 x 的根
        int r2 = find(y, fa);   // 找到 y 的根
        fa[r1] = r2;            // 将 r1 的根设为 r2（合并两棵树）
    }
}
