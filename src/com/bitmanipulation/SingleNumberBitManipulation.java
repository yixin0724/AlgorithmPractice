package com.bitmanipulation;

import java.util.Scanner;

/**
 * LeetCode Hot 100 - 136. Single Number (只出现一次的数字)
 *
 * ═══════════════════════════════════════════════════════════
 * 人为思考过程：
 * ═══════════════════════════════════════════════════════════
 * 1. 问题本质：给定一个非空整数数组，除了某个元素只出现一次以外，
 *    其余每个元素均出现两次。找出那个只出现了一次的元素。
 * 2. 朴素方案：哈希表统计频次，O(n) 时间 + O(n) 空间。或者排序后
 *    找孤立元素，O(n log n) 时间。但题目要求线性时间且常数空间。
 * 3. 关键突破口：能用 O(1) 额外空间做什么操作能"抵消"成对出现的数字？
 *    回忆异或（XOR）运算的性质：
 *    - a ^ a = 0（相同数字异或得 0，成对出现会互相抵消）
 *    - a ^ 0 = a（任何数与 0 异或等于自身）
 *    - 异或满足交换律和结合律（与顺序无关）
 * 4. 因此，如果将所有数字全部异或起来：成对出现的数字会两两相消变为 0，
 *    而落单的那个数字与 0 异或仍是它自己。最终结果就是答案。
 * 5. 这个解法极其简洁：遍历一遍数组，不断异或即可。不需要额外数据结构，
 *    时间和空间都达到了最优。
 *
 * ═══════════════════════════════════════════════════════════
 * 具体措施（算法步骤）：
 * ═══════════════════════════════════════════════════════════
 * 1. 初始化结果变量 result = 0。
 * 2. 遍历数组中的每个元素 num：
 *    - 执行 result = result ^ num（异或运算）。
 * 3. 遍历结束后，result 的值即为只出现一次的数字。
 * 4. 输出 result。
 *
 * ═══════════════════════════════════════════════════════════
 * 复杂度分析：
 * ═══════════════════════════════════════════════════════════
 * 时间复杂度：O(n)
 *   - 只需遍历数组一次，每次异或操作为 O(1)。
 * 空间复杂度：O(1)
 *   - 只使用一个结果变量 result，不依赖输入规模。
 */
public class SingleNumberBitManipulation {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        System.out.println("请输入数组长度：");
        int n = scanner.nextInt();
        int result = 0;
        System.out.println("请输入数组元素：");
        // 边读入边异或，利用 a^a=0 和 a^0=a 的性质
        for (int i = 0; i < n; i++) {
            result ^= scanner.nextInt();  // 成对数字互相抵消，落单数字保留
        }
        System.out.println("只出现一次的数字为：" + result);
    }
}
